FRAMES = [
    r"     _.-'`'-._     ",
    r"  .-'  _   _  '-.  ",
    r" /    (_) (_)    \ ",
    r" |  .-.'   '.-.  | ",
    r" | (  (  O  )  ) | ",
    r" |  '-.'___'.-'  | ",
    r" \             / ",
    r"  '-._     _.-'  ",
    r"      '---'       ",
]


def render_static() -> str:
    return "\n".join(FRAMES)


def animate(repeat_delay: float = 0.1) -> None:
    frames = [
        "\n".join(FRAMES),
        "\n".join(FRAMES[:-1] + ["     '---'       "]),
    ]
    for frame in itertools.cycle(frames):
        print("\033[2J\033[H", end="")
        print(frame)
        time.sleep(repeat_delay)

